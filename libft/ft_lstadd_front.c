#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (*lst == NULL)
		*lst = new;
	else if (new == NULL)
		return ;
	else
	{
		new->next = *lst;
		*lst = new;
	}
}
